    public void addProducto(Producto p, int lista) {
        boolean flag = false;
        DefaultListModel modelo = (DefaultListModel) getLista(lista).getModel();
        if (modelo.size() == 0) {
            modelo.addElement(p);
        } else {
            for (int i = 0; i < modelo.size(); i++) {
                if (p.getId() == ((Producto) modelo.elementAt(i)).getId()) {
                    p.setCantidadCarrito(((Producto) modelo.elementAt(i)).getCantidadCarrito() + 1);
                    modelo.setElementAt(p, i);
                    flag = true;
                    break;
                }
            }
            if (!flag) {
                modelo.addElement(p);
            }
        }
        getLista(lista).setModel(modelo);
    }

    public void accionComprar(int n) {
        switch (n) {
            case 0:
                JOptionPane.showMessageDialog(this, "Compra efectuada exitosamente \n Gracias por su visita!");
                break;
            case 1:
                int c = (int) (Math.random() * 10) + 1;
                int p = (int) (Math.random() * 10) + 1;
                JOptionPane.showMessageDialog(this, "La caja #" + c + " tiene la fila mas corta... \n Con " + p + " persona(s) adelante");
                break;
            case 2:
                break;
            case 3:
                JOptionPane.showMessageDialog(this, "Lista de productos enviada a su supermercado de preferencia \n Retirar compra en 30 minutos");
                break;
        }
    }

